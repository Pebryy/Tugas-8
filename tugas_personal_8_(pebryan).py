# Membuat class Queue menggunakan Circular Queue
class Queue:

    # Constructor
    def __init__(self, maxSize):
        # Membuat list dengan isi awal None
        self.items = maxSize * [None]
        # Menyimpan kapasitas maksimal queue
        self.maxSize = maxSize
        # Pointer elemen pertama
        self.start = -1
        # Pointer elemen terakhir
        self.top = -1

    # Menampilkan isi queue
    def __str__(self):
        values = [str(x) for x in self.items]
        return ' '.join(values)

    # Mengecek apakah queue penuh
    def isFull(self):
        # Kondisi circular queue penuh
        if self.top + 1 == self.start:
            return True
        # Kondisi saat top di akhir list dan start di awal
        elif self.start == 0 and self.top + 1 == self.maxSize:
            return True
        else:
            return False

    # Mengecek apakah queue kosong
    def isEmpty(self):
        if self.top == -1:
            return True
        else:
            return False

    # Menambahkan data ke queue
    def enqueue(self, value):
        # Jika queue penuh
        if self.isFull():
            return "The queue is full"
        else:
            # Jika top sudah di index terakhir
            # kembali ke index awal
            if self.top + 1 == self.maxSize:
                self.top = 0
            else:
                # Geser top ke kanan
                self.top += 1
                # Jika queue sebelumnya kosong
                if self.start == -1:
                    self.start = 0
            # Menyimpan data ke queue
            self.items[self.top] = value
            return "The element is inserted at the end of Queue"

    # Menghapus data paling depan queue
    def dequeue(self):
        # Jika queue kosong
        if self.isEmpty():
            return "There is not any element in the Queue"
        else:
            # Mengambil elemen pertama
            firstElement = self.items[self.start]
            # Menyimpan posisi start lama
            start = self.start
            # Jika hanya ada 1 elemen
            if self.start == self.top:
                self.start = -1
                self.top = -1
            # Jika start berada di akhir list
            elif self.start + 1 == self.maxSize:
                self.start = 0
            else:
                # Geser start ke kanan
                self.start += 1
            # Menghapus data lama
            self.items[start] = None
            # Mengembalikan data yang dihapus
            return firstElement

    # Melihat elemen paling depan
    def peek(self):
        # Jika queue kosong
        if self.isEmpty():
            return "There is not any element in the Queue"
        else:
            return self.items[self.start]

    # Menghapus seluruh isi queue
    def delete(self):
        # Mengisi ulang queue dengan None
        self.items = self.maxSize * [None]
        # Reset pointer
        self.top = -1
        self.start = -1

