# Membuat class Node untuk menyimpan data dan alamat node berikutnya
class Node:
    def __init__(self, value=None):
        # Menyimpan nilai/data node
        self.value = value

        # Pointer ke node berikutnya
        self.next = None

    # Method untuk mengubah object menjadi string
    def __str__(self):
        return str(self.value)


# Membuat class LinkedList
class LinkedList:
    def __init__(self):
        # Node pertama
        self.head = None

        # Node terakhir
        self.tail = None

    # Agar LinkedList bisa di-loop menggunakan for
    def __iter__(self):
        curNode = self.head

        # Loop selama masih ada node
        while curNode:
            yield curNode
            curNode = curNode.next

# Membuat class Queue menggunakan LinkedList
class Queue:
    def __init__(self):
        # Queue menggunakan LinkedList
        self.linkedList = LinkedList()

    # Menampilkan isi queue
    def __str__(self):
        values = [str(x) for x in self.linkedList]
        return ' '.join(values)

    # Menambahkan data ke belakang queue
    def enqueue(self, value):
        newNode = Node(value)

        # Jika queue kosong
        if self.linkedList.head == None:
            self.linkedList.head = newNode
            self.linkedList.tail = newNode

        # Jika queue sudah memiliki data
        else:
            self.linkedList.tail.next = newNode
            self.linkedList.tail = newNode

    # Mengecek apakah queue kosong
    def isEmpty(self):
        if self.linkedList.head == None:
            return True
        else:
            return False

    # Menghapus data paling depan (FIFO)
    def dequeue(self):

        # Jika queue kosong
        if self.isEmpty():
            return "There is not any node in the Queue"

        else:
            tempNode = self.linkedList.head

            # Jika hanya ada 1 node
            if self.linkedList.head == self.linkedList.tail:
                self.linkedList.head = None
                self.linkedList.tail = None

            # Jika ada lebih dari 1 node
            else:
                self.linkedList.head = self.linkedList.head.next

            return tempNode

    # Melihat data paling depan tanpa menghapus
    def peek(self):

        # Jika queue kosong
        if self.isEmpty():
            return "There is not any node in the Queue"

        else:
            return self.linkedList.head

    # Menghapus seluruh isi queue
    def delete(self):
        self.linkedList.head = None
        self.linkedList.tail = None

