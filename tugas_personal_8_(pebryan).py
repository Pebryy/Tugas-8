# jika kapasitas stack utama sudah penuh

class PlateStack():
    # Constructor
    def __init__(self, capacity):
        # Kapasitas maksimal tiap stack
        self.capacity = capacity
        # Menyimpan semua stack
        self.stacks = []

    # Menampilkan isi stack
    def __str__(self):
        return self.stacks

    # Menambahkan data ke stack
    def push(self, item):
        # Jika masih ada stack
        # dan stack terakhir belum penuh
        if len(self.stacks) > 0 and (len(self.stacks[-1])) < self.capacity:
            # Tambahkan item ke stack terakhir
            self.stacks[-1].append(item)
        else:
            # Jika penuh, buat stack baru
            self.stacks.append([item])

    # Menghapus data dari stack terakhir
    def pop(self):
        # Menghapus stack kosong di akhir
        while len(self.stacks) and len(self.stacks[-1]) == 0:
            self.stacks.pop()
        # Jika semua stack kosong
        if len(self.stacks) == 0:
            return None
        else:
            # Menghapus item paling atas
            return self.stacks[-1].pop()

    # Menghapus data dari stack tertentu
    def pop_at(self, stackNumber):
        # Jika stack masih memiliki data
        if len(self.stacks[stackNumber]) > 0:
            # Menghapus item paling atas
            return self.stacks[stackNumber].pop()
        else:
            return None

