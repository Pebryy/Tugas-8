# Membuat class Stack
class Stack():
    # Constructor
    def __init__(self):
        # List untuk menyimpan data stack
        self.list = []

    # Mengembalikan jumlah data dalam stack
    def __len__(self):
        return len(self.list)

    # Menambahkan data ke atas stack
    def push(self, item):
        self.list.append(item)

    # Menghapus data paling atas stack
    def pop(self):
        # Jika stack kosong
        if len(self.list) == 0:
            return None
        # Menghapus data terakhir
        return self.list.pop()


# Membuat Queue menggunakan dua stack
class QueueviaStack():
    # Constructor
    def __init__(self):
        # Stack untuk data masuk
        self.inStack = Stack()
        # Stack untuk membalik urutan data
        self.outStack = Stack()

    # Menambahkan data ke queue
    def enqueue(self, item):
        self.inStack.push(item)

    # Menghapus data paling depan queue
    def dequeue(self):
        # Memindahkan semua data dari inStack ke outStack
        # agar urutan menjadi FIFO
        while len(self.inStack):
            self.outStack.push(self.inStack.pop())
        # Mengambil data paling depan
        result = self.outStack.pop()
        # Mengembalikan data ke inStack
        while len(self.outStack):
            self.inStack.push(self.outStack.pop())
        # Mengembalikan hasil dequeue
        return result

