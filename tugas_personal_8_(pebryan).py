class Stack():
    # Constructor Stack
    def __init__(self):
        # Node paling atas stack
        self.top = None
        # Menyimpan node minimum
        self.minNode = None

    # Mengambil nilai minimum dalam stack
    def min(self):
        # Jika stack kosong
        if not self.minNode:
            return None
        # Mengembalikan nilai minimum
        return self.minNode.value

    # Menambahkan data ke stack
    def push(self, item):
        # Jika sudah ada minNode
        # dan nilai minimum lebih kecil dari item baru
        if self.minNode and (self.minNode.value < item):
            # Simpan nilai minimum sebelumnya
            self.minNode = Node(
                value=self.minNode.value,
                next=self.minNode
            )
        else:
            # Jika item baru lebih kecil
            # jadikan sebagai minimum baru
            self.minNode = Node(
                value=item,
                next=self.minNode
            )
        # Menambahkan item ke top stack
        self.top = Node(value=item, next=self.top)

    # Menghapus data paling atas stack
    def pop(self):
        # Jika stack kosong
        if not self.top:
            return None
        # Memindahkan minNode ke berikutnya
        self.minNode = self.minNode.next
        # Mengambil nilai top
        item = self.top.value
        # Memindahkan top ke node berikutnya
        self.top = self.top.next
        # Mengembalikan nilai yang dihapus
        return item

