# Mengurutkan stack dengan nilai terkecil berada di atas
# Menggunakan hanya satu stack sementara

# Function untuk mengurutkan stack
def sort_stack(stack):

    # Mengambil dua data pertama dari stack
    previous = stack.pop()
    current = stack.pop()
    # Stack sementara
    temp = Stack()

    # Proses pengurutan awal
    while current:
        # Jika previous lebih kecil dari current
        if previous < current:
            # Simpan previous ke temp
            temp.push(previous)
            # Update previous
            previous = current
            # Ambil data berikutnya
            current = stack.pop()
        else:
            # Simpan current ke temp
            temp.push(current)
            # Ambil data berikutnya
            current = stack.pop()
        # Jika sudah akhir stack
        if current == None and previous:
            temp.push(previous)

    # Menandai apakah stack sudah terurut
    sorted = True

    # Mengambil data dari temp
    previous = temp.pop()
    current = temp.pop()

    # Mengembalikan data ke stack utama
    while current:
        # Jika previous lebih besar dari current
        if previous > current:
            # Masukkan previous ke stack utama
            stack.push(previous)
            # Update previous
            previous = current
            # Ambil data berikutnya
            current = temp.pop()
        else:
            # Masukkan current ke stack utama
            stack.push(current)
            # Ambil data berikutnya
            current = temp.pop()
            # Menandakan stack belum sepenuhnya terurut
            sorted = False
        # Jika sudah akhir temp stack
        if current == None and previous:
            stack.push(previous)
    # Jika sudah terurut
    if sorted:
        return stack
    # Jika belum terurut, ulangi sorting
    else:
        return sort_stack(stack)


# Membuat class Stack
class Stack():
    # Constructor
    def __init__(self):
        # Node paling atas
        self.top = None

    # Menampilkan isi stack
    def __str__(self):
        return str(self.top)

    # Menambahkan data ke stack
    def push(self, item):
        self.top = current(item, self.top)

    # Menghapus data paling atas stack
    def pop(self):
        # Jika stack kosong
        if not self.top:
            return None
        # Mengambil node teratas
        item = self.top
        # Memindahkan top ke node berikutnya
        self.top = self.top.next
        # Mengembalikan data
        return item.data


# Membuat class node
class current():

    # Constructor node
    def __init__(self, data=None, next=None):
        # Menyimpan data
        self.data = data
        # Pointer ke node berikutnya
        self.next = next

    # Menampilkan node sebagai string
    def __str__(self):
        return str(self and self.data) + ',' + str(self and self.next)
